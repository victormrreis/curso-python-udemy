# Operadores de Comparação (Relacionais)
# OP      Significado      Exemplo (True)
# >       Maior que        10 > 5
# >=      Maior ou igual   10 >= 10
# <       Menor que        5 < 10
# <=      Menor ou igual   5 <= 5
# ==      Igual            10 == 10
# !=      Diferente        10 != 5
# Também se aplicam a outros tipos de dados, como strings, por exemplo.


maior = 10 > 5
maior_ou_igual = 10 >= 10
menor = 5 < 10
menor_ou_igual = 5 <= 5
igual = 10 == 10
diferente = 10 != 5
print(diferente)